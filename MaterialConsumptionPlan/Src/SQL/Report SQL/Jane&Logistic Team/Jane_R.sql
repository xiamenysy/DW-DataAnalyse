  SO_ITEM.PLANTID,
  SO_ITEM.SALES_ORG,
  SO_ITEM.MATERIALID,
  ITEM_BSC.CATALOG_DASH,
  ITEM_BSC.MAT_DESC,
  ITEM_BSC.PROD_BU,
  ITEM_BSC.PROD_FAM,
  ITEM_BSC.MATL_TYPE,
  ITEM_BSC.STRATEGY_GRP,
  ITEM_BSC.MRP_TYPE
FROM
  (SELECT MATERIALID
    ||'_'
    ||PLANTID AS ID,
    MATERIALID,
    PLANTID,
    SALES_ORG
  FROM INV_SAP_SALES_HST
  WHERE SALES_ORG = '5003'
  )SO_ITEM
LEFT JOIN
  (SELECT ID,
    CATALOG_DASH,
    MAT_DESC,
    PROD_BU,
    PROD_FAM,
    MATL_TYPE,
    STRATEGY_GRP,
    MRP_TYPE
  FROM INV_SAP_PP_OPT_X
