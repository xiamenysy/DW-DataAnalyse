SELECT 
  MVK_OPT.PLANT,
  MVK_OPT.SALES_ORG,
  MVK_OPT.MATERIALID,
  ISMC.CATALOG_STRING1 AS CATALOG_DASH,
  MVK_OPT.MAT_DESC,
  MVK_OPT.PROD_BU,
  MVK_OPT.MATL_TYPE_MTART,
  MVK_OPT.STRATEGY_GRP,
  MVK_OPT.MRP_TYPE,
  MVK_OPT.ULTIMATE_SOURCE
FROM
  (SELECT MVK.MATERIALID,
    MVK.SALES_ORG,
    OPT.PLANT,
    OPT.MAT_DESC,
    OPT.ULTIMATE_SOURCE,
    OPT.PROD_BU,
    OPT.MATL_TYPE_MTART,
    OPT.STRATEGY_GRP,
    OPT.MRP_TYPE
  FROM
    (SELECT MATERIALID,
      SALES_ORG
    FROM INV_SAP_PP_MVKE
    WHERE SALES_ORG    = '5003'
    AND CURRENT_SERIES = 'X'
    )MVK
  LEFT JOIN
    (SELECT MATERIALID,
      MAT_DESC,
      (PLANTID - 1) AS PLANT,
      SUBSTR(PROD_BU,0,3) AS PROD_BU,
      ULTIMATE_SOURCE,
      MATL_TYPE_MTART,
      STRATEGY_GRP,
      MRP_TYPE
    FROM INV_SAP_PP_OPTIMIZATION
    )OPT
  ON OPT.MATERIALID = MVK.MATERIALID
  )MVK_OPT
LEFT JOIN
  (SELECT CATALOG_STRING1,
    CATALOG_STRING2,
    MATERIALID
  FROM INV_SAP_NODASH_MAT_CATA
  )ISMC
ON ISMC.MATERIALID = MVK_OPT.MATERIALID;


