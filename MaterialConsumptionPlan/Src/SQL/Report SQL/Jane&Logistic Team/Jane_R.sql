--PO/SO ITEM LIST
--PO
SELECT DISTINCT PO_ITEM.ID,
  PO_ITEM.PLANTID,
  PO_ITEM.MATERIALID,
  ITEM_BSC.CATALOG_DASH,
  ITEM_BSC.MAT_DESC,
  ITEM_BSC.PROD_BU,
  ITEM_BSC.PROD_FAM,
  ITEM_BSC.MATL_TYPE,
  ITEM_BSC.STRATEGY_GRP,
  ITEM_BSC.MRP_TYPE
FROM
  (SELECT MATERIALID
    ||'_'
    ||PLANTID AS ID,
    MATERIALID,
    PLANTID
  FROM INV_SAP_PP_PO_HISTORY
  WHERE PLANTID = '5040'
  )PO_ITEM
LEFT JOIN
  (SELECT ID,
    CATALOG_DASH,
    MAT_DESC,
    PROD_BU,
    PROD_FAM,
    MATL_TYPE,
    STRATEGY_GRP,
    MRP_TYPE
  FROM INV_SAP_PP_OPT_X
  )ITEM_BSC
ON ITEM_BSC.ID = PO_ITEM.ID;
--SO
