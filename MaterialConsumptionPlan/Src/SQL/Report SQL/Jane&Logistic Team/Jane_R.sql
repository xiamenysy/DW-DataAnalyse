  PLANT,
  MATERIAL,
  CATALOG_DASH,
  MAT_DESC,
  PROD_BU,
  PROD_FAM,
  MATL_TYPE,
  STRATEGY_GRP,
  MRP_TYPE,
  ULTIMATE_SOURCE
FROM INV_SAP_PP_OPT_X
