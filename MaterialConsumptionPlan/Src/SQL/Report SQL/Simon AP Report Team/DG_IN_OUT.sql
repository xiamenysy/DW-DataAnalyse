        ||'_'
        ||MATERIALID AS PO_ID,
        SUM(COMMITTEDQTY) AS OPEN_QTY
      FROM INV_SAP_PP_PO_HISTORY
      WHERE DELIVERYCOMPLETE IS NULL
      GROUP BY MATERIALID, PLANTID, EBELNPURCHDOCNO
      )PO_OPEN
    ON PO_OPEN.PO_ID = PO_BS.PO_ID
     )
  )
GROUP BY ID,
  MATERIALID;   
  

