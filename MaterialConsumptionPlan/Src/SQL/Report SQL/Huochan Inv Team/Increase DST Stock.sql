      (SELECT MATERIALID
        ||'_'
        ||PLANTID                           AS ID,
        MATERIALID                          AS MATERIALID,
        PLANTID                             AS PLANTID,
        CEIL(SUM(PLNMG_PLANNEDQUANTITY)/13) AS FC_AVG13_WEEK_QTY
