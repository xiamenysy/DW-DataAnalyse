      AND VERSBP_VERSION = '55'
      GROUP BY MATERIALID,
        PLANTID
      )FC_AVG13_WEEK
    ON FC_AVG13_WEEK.ID = ITEM_BASIC_PP.ID
    )TOT
  LEFT JOIN
    (SELECT MATERIALID
      ||'_'
      ||PLANTID AS ID,
      MATERIALID,
      PLANTID,
      SUM(DELIVERY_QTY_SUOM) AS DELIVERY_QTY_SUOSUM
    FROM INV_SAP_LIKP_LIPS_DAILY
    WHERE CREATED_ON_DATE BETWEEN TO_CHAR(SYSDATE - 182) AND TO_CHAR(SYSDATE)
    GROUP BY MATERIALID,
      PLANTID
    )DELI
  ON DELI.ID = TOT.ID
  )
